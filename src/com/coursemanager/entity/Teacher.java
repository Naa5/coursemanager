package com.coursemanager.entity;

public class Teacher {
	private String t_id;
	private String t_no;
	private String t_name;
	private String t_password;
	private String t_titles;
	private String t_office;
	private String t_dept;
	private String t_type;
	public String getT_id() {
		return t_id;
	}
	public void setT_id(String t_id) {
		this.t_id = t_id;
	}
	public String getT_no() {
		return t_no;
	}
	public void setT_no(String t_no) {
		this.t_no = t_no;
	}
	public String getT_name() {
		return t_name;
	}
	public void setT_name(String t_name) {
		this.t_name = t_name;
	}
	public String getT_password() {
		return t_password;
	}
	public void setT_password(String t_password) {
		this.t_password = t_password;
	}
	public String getT_titles() {
		return t_titles;
	}
	public void setT_titles(String t_titles) {
		this.t_titles = t_titles;
	}
	public String getT_office() {
		return t_office;
	}
	public void setT_office(String t_office) {
		this.t_office = t_office;
	}
	public String getT_dept() {
		return t_dept;
	}
	public void setT_dept(String t_dept) {
		this.t_dept = t_dept;
	}
	public String getT_type() {
		return t_type;
	}
	public void setT_type(String t_type) {
		this.t_type = t_type;
	}
	
}
